def get_popularity_score(card_name: str) -> int:
    """Return a 0-100 popularity score for a card name.

    Tries exact match first, then longest-key substring so that
    'Charizard ex' resolves to 'Charizard' (score 100) rather than
    a shorter accidental match.
    """
    name_lower = card_name.strip().lower()

    for poke, score in POPULARITY_SCORES.items():
        if poke.lower() == name_lower:
            return score

    # Longest substring wins (avoids 'Char' shadowing 'Charmeleon')
    candidates = [
        (k, v) for k, v in POPULARITY_SCORES.items()
        if k.lower() in name_lower
    ]
    if candidates:
        return max(candidates, key=lambda x: len(x[0]))[1]

    return DEFAULT_SCORE


def get_popularity_mult(card_name: str) -> float:
    return score_to_mult(get_popularity_score(card_name))


